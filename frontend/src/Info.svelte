<h3> Gestor Descargas QR</h3>
<p>Este programa se fundamenta en la API de https://0x0.st/ para la gestión de los archivos subidos. Visita su web para conocer los términos y condiciones de uso en detalle.</p>
<p>El tamaño máximo adminitido para los archivos es 512.0 MiB</p>
<p>No se permite alojar archivos del siguiente tipo: application/x-dosexec, application/x-executable, application/x-sharedlib, application/x-hdf5, application/java-archive, application/vnd.android.package-archive, application/x-rar, application/vnd.microsoft.portable-executable</p>
<p>Debes tener en cuenta también las siguientes normas sobre el material no permitido:</p>
<ul>
<li>piracy</li>
<li>pornography and gore</li>
<li>extremist material of any kind</li>
<li>malware / botnet C&C</li>
<li>anything related to crypto currencies</li>
<li>backups (yes, this includes your minecraft stuff, seriously people have been dumping terabytes of it here for years)</li>
<li>CI build artifacts</li>
<li>doxxing, database dumps containing personal information</li>
<li>anything illegal under German law</li>
</ul>

